    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
            
        String codigo = jTF_Codigo.getText();
        String nome = jTF_Nome.getText();
        String email = jTF_Email.getText();
        String cpf = jTF_cpf.getText();
        String telefone  = jTF_Telefone.getText();
        String rg = jTF_RG.getText();
        String cidade = jTF_cidade.getText();
        String estado = jTF_estado.getText();
        String CNH  = jTF_Cnh.getText();
        
        //int cp = Integer.parseInt(cpf);
        
        if(nome.equals("")){
            JOptionPane.showMessageDialog(null,"nenhum campo pode estar vazio",
                    "Concessionaria", JOptionPane.WARNING_MESSAGE);
        }else {
        Connection con = Conexao.AbrirConexao();
        int n =  Integer.parseInt(codigo);
        ClienteDAO sql = new ClienteDAO(con);
        Cliente a = new Cliente();
        
        a.setId_cliente(n);
        a.setNome(nome);
        a.setEmail(email);
        a.setCpf(cpf);
        a.setTelefone(telefone);
        a.setRg(rg);
        a.setCidade(cidade);
        a.setEstado(estado);
        a.setCnh(CNH);
        
        sql.Alterar_Cliente(a);
        Conexao.FecharConexao(con);
       
        jTF_Codigo.setText("");
        jTF_Nome.setText("");
        jTF_Email.setText("");
        jTF_cpf.setText("");
        jTF_Telefone.setText("");
        jTF_RG.setText("");
        jTF_cidade.setText("");
        jTF_estado.setText("");
        jTF_Cnh.setText("");
        
        JOptionPane.showMessageDialog(null,"AlteraÃ§Ã£o realizada com sucesso",
                "Concessionaria", JOptionPane.INFORMATION_MESSAGE);
        dispose();
        
        }
    }//GEN-LAST:event_AlterarActionPerformed

